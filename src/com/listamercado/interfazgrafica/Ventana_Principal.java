    private void Boton_InsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Boton_InsertarActionPerformed

        if (verificarCampos() && rutaImagen != null) {
            try {
                Connection cn = Conexion.getConnection();
                //System.out.println("**"+txt_nombre.getText() +"**"+ txt_precio.getText() +"**"+ rutaImagen);
                PreparedStatement pst = cn.prepareStatement("INSERT INTO productos(nombre,precio,add_fecha,imagen) VALUES(?,?,?,?)");
                //pst.setString(1, "");
                pst.setString(1, txt_nombre.getText());
                pst.setString(2, txt_precio.getText());

                SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
                String addFecha = formatoFecha.format(txt_AddFecha.getDate());
                pst.setString(3, addFecha);

                InputStream img = new FileInputStream(new File(rutaImagen));
                pst.setBlob(4, img);
                pst.executeUpdate();
                
                mostrarProductos();
                JOptionPane.showMessageDialog(null, "Dato ingresado");

            } catch (HeadlessException | FileNotFoundException | SQLException e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor completa los campos");
        }
    }//GEN-LAST:event_Boton_InsertarActionPerformed

    private void Boton_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Boton_ActualizarActionPerformed
        if (verificarCampos() && txt_id.getText() != null) {

            Connection cn = Conexion.getConnection();
            if (rutaImagen == null) {
                try {
                    PreparedStatement pst = cn.prepareStatement("UPDATE productos SET nombre = ?, precio= ?,"
                            + "add_fecha = ? WHERE id = ?");
                    pst.setString(1, txt_nombre.getText());
                    pst.setString(2, txt_precio.getText());

                    SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
                    String addFecha = formatoFecha.format(txt_AddFecha.getDate());
                    pst.setString(3, addFecha);

                    pst.setInt(4, Integer.parseInt(txt_id.getText()));
                    pst.executeUpdate();
                    
                    mostrarProductos();
                    JOptionPane.showMessageDialog(null, "Actualizacion exitosa");
                } catch (HeadlessException | SQLException e) {
                    e.printStackTrace();
                }
            } else {
                try {
                    PreparedStatement pst = cn.prepareStatement("UPDATE productos SET nombre = ?, precio= ?,"
                            + "add_fecha = ?, imagen = ? WHERE id = ?");
                    pst.setString(1, txt_nombre.getText());
                    pst.setString(2, txt_precio.getText());

                    SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
                    String addFecha = formatoFecha.format(txt_AddFecha.getDate());
                    pst.setString(3, addFecha);

                    InputStream img = new FileInputStream(new File(rutaImagen));
                    pst.setBlob(4, img);

                    pst.setString(5, txt_id.getText());
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Actualizacion exitosa");
                } catch (FileNotFoundException | SQLException e) {
                    e.printStackTrace();
                }
            }
        }else{
            JOptionPane.showMessageDialog(null,"Algunos campos estan vacios.");
        }
    }//GEN-LAST:event_Boton_ActualizarActionPerformed

    private void Boton_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Boton_EliminarActionPerformed
        if(!txt_id.getText().equals("")){
            try {
                Connection cn = Conexion.getConnection();
                PreparedStatement pst = cn.prepareStatement("DELETE FROM productos WHERE id = ?");
                
                pst.setInt(1, Integer.parseInt(txt_id.getText()));
                pst.executeUpdate();
                mostrarProductos();
                JOptionPane.showMessageDialog(null, "Registro Eliminado");
            } catch (HeadlessException | NumberFormatException | SQLException e) {
                e.printStackTrace();
        }
        }else{
            JOptionPane.showMessageDialog(null,"Ingresa una ID");
        }
    }//GEN-LAST:event_Boton_EliminarActionPerformed

