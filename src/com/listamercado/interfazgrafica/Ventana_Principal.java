    public boolean verificarCampos() {
        if (txt_nombre.getText() == null || txt_precio.getText() == null
                || txt_AddFecha.getDate() == null) {
            return false;
        } else {
            try {
                Float.parseFloat(txt_precio.getText());
                return true;
            } catch (Exception e) {
                return false;
            }
        }
    }
    
    //Crear arraylist para llenar la tabla
    public ArrayList<Producto> getProductList(){
        ArrayList<Producto> listaProducto = new ArrayList<Producto>();
        try{
            Connection cn = Conexion.getConnection();
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM productos");
            Producto producto;
            
            while(rs.next()){
                producto = new Producto(rs.getInt("id"), rs.getString("nombre"), Float.parseFloat(rs.getString("precio")), 
                        rs.getString("add_fecha"), rs.getBytes("imagen"));
                listaProducto.add(producto);
            }
            
            
            
        }catch(Exception e){
            e.printStackTrace();
        }
        return listaProducto;
    }
    
    //Llenar la tabla con el arraylist creado
    public void mostrarProductos(){
        ArrayList<Producto> lista = getProductList();
        DefaultTableModel modelo = (DefaultTableModel)Tabla_Productos.getModel();
        //limpiar la tabla
        modelo.setRowCount(0);
        Object[] row = new Object[4];
        
        for(int i = 0; i < lista.size(); i++){
            row[0] = lista.get(i).getId();
            row[1] = lista.get(i).getNombre();
            row[2] = lista.get(i).getPrecio();
            row[3] = lista.get(i).getAddFecha();
            
            modelo.addRow(row);
        }
                
    }
    
    public void mostrarItem(int index){
        txt_id.setText(Integer.toString(getProductList().get(index).getId()));
        txt_nombre.setText(getProductList().get(index).getNombre());
        txt_precio.setText(Float.toString(getProductList().get(index).getPrecio()));
        try{
        Date addDate = null;
        addDate = new SimpleDateFormat("yyyy-MM-dd").parse((String)getProductList().get(index).getAddFecha());
        txt_AddFecha.setDate(addDate);
        }catch(ParseException e){
            e.printStackTrace();
        }
        label_imagen.setIcon(Tamaño.EstablecerTamaño(null, getProductList().get(index).getImagen()));
    }
